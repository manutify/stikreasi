<p>Stiker <strong>die cut</strong> adalah stiker yang dipotong <strong>tembus</strong> hingga ke lapisan kertas belakang (<em>backing paper</em>). Bayangkan seperti mencetak kue menggunakan cetakan—pisau pemotong menekan bahan stiker dan kertas pendukungnya sekaligus, mengikuti persis bentuk desain.</p>

